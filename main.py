# Soal No 1
hargaLaptop = 7500000
diskon = 20/100
pajak = 5/100
hargaDiskon = hargaLaptop - (hargaLaptop * diskon)
hargaPajak = hargaDiskon * pajak
hargaAkhir = hargaDiskon + hargaPajak
print (hargaAkhir)

# Soal No 2
gaji = 6000000
bonus = 10/100
pajak = 8/100
gajiTotal = gaji + (gaji * bonus)
gajiPajak = gajiTotal * pajak
gajiAkhir = gajiTotal - gajiPajak
print (gajiAkhir)

